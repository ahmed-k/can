public class CoordinateZone {




        public CoordinateZone(float xStart, float xEnd, float yStart, float yEnd) {

            this.xStart = xStart;
            this.xEnd   = xEnd;
            this.yStart = yStart;
            this.yEnd = yEnd;
        }
        private float xStart;
        private float xEnd;
        private float yStart;
        private float yEnd;


    }

